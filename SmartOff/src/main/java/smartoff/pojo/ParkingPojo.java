package smartoff.pojo;

public class ParkingPojo {
	public ParkingPojo(){}
	
	
	private String empID;
	private String slotID;
	public String getEmpID() {
		return empID;
	}
	public void setEmpID(String empID) {
		this.empID = empID;
	}
	public String getSlotID() {
		return slotID;
	}
	public void setSlotID(String slotID) {
		this.slotID = slotID;
	}
	
	
	

}